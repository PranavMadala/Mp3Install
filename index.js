function show1password() {
	var show =document.getElementById("password");
	if (show.type=='password') {
	show.type='text'

	}
	else{
	show.type='password'
	}

}
function showpassword() {
	var show =document.getElementById("confirm");
	if (show.type=='confirm') {
	show.type='text'

	}
	else{
	show.type='confirm'
	}
}
function password(){
	document.HTMLelement.text("password").inner.html
}
function confirm(){
	document.HTMLelement.text("confirm").inner.html
}